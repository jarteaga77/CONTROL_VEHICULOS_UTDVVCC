     public void cargarItemVehiculo()
    {
        ControllerItemList controller=new ControllerItemList();
        
        ArrayList lista=controller.listarItemVehiculo();
        
        cb_vehiculo.removeAllItems();
        if (lista!=null)
        {
            int size=lista.size();
            
            for(int i=0; i<size; i++)
            {
                cb_vehiculo.addItem(lista.get(i));
            }
        }
    }
     
     public void limpiar()
     
     {
         txt_obse.setText("");
         cargarIdSiguiente();
     }

     public void cargarIdSiguiente()
     {
       conexion=new ConexionBD();
      ResultSet obj=conexion.executeQuery("SELECT MAX(id_notan)+1 as id  FROM consumo_combustible.no_tanqueo");
       
        try {
            while(obj.next())
            {
                txt_id.setText(String.valueOf(obj.getInt("id")));//Le asigna el consecutivo siguiente de orden compra al textfield
            }} catch (SQLException ex) {
            Logger.getLogger(JIFRegNoTanqueo.class.getName()).log(Level.SEVERE, null, ex);
        }
        
     }





