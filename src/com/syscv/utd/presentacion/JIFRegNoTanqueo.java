    private void btn_aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_aceptarActionPerformed
     
        try
        {
        
        int id_year=0;
        int idvehiculo;
       
        mes=mc_mes.getMonth()+1;
        year=yc_year.getYear();
        
        ResultSet obj=conexion.executeQuery("SELECT id_year FROM utilidades.year WHERE year="+year+"");
        
        while (obj.next())
        {
            id_year=obj.getInt("id_year");
        }
        
         Vehiculo v=(Vehiculo) cb_vehiculo.getSelectedItem();
         idvehiculo=v.getId_vehiculo();
         
         objeto=new NoTanqueo();
         
         objeto.setIdvehiculo(idvehiculo);
         objeto.setId_mes(mes);
         objeto.setId_year(id_year);
         objeto.setTotal_crtl(Integer.parseInt(txt_total_pesos.getText()));
         objeto.setTotal_gal(Integer.parseInt(txt_total_gal.getText()));
         objeto.setObservaciones(txt_obse.getText());
         objeto.setFechaTran(new Date());
         objeto.setIdusuario(idusuario);
         
         
         daoObjeto=new DaoNoTanqueo();
         
         daoObjeto.guardar(objeto);
         
         JOptionPane.showMessageDialog(this, "Se Registro con exito El no tanqueo");
         limpiar();
        
       }catch(Exception e)
       {
           JOptionPane.showMessageDialog(this, e);
       }
    }//GEN-LAST:event_btn_aceptarActionPerformed

