public void consultar() throws SQLException
{
  try
        {
        
        conexion=new ConexionBD();
        
        
        ResultSet obj=conexion.executeQuery("SELECT c.id_cos_fact, c.fecha_reg, m.descripcion, y.year,c.total_fact, us.usuario FROM mantenimientos_mecanica.cos_fact_mto_ext c "
                + " JOIN utilidades.mes m ON m.id_mes=c.id_mes JOIN utilidades.year y ON y.id_year=c.id_anio JOIN utilidades.usuarios us ON us.id_usuario=c.id_usuario ORDER BY c.id_cos_fact DESC");
    
        
         while (obj.next()) 
        {
        
        Object [] datos = new Object[6];
        
     
        for (int i=0;i<6;i++)
        {
            datos[i] =obj.getObject(i+1);
        }

         modelo.addRow(datos);
       
        }
           tb_consulta.setModel(modelo);
           
        TableColumnModel columnModel =tb_consulta.getColumnModel();
        columnModel.getColumn(0).setPreferredWidth(5);
        columnModel.getColumn(1).setPreferredWidth(20);
        columnModel.getColumn(2).setPreferredWidth(20);
        columnModel.getColumn(3).setPreferredWidth(20);
        columnModel.getColumn(4).setPreferredWidth(50);
         columnModel.getColumn(5).setPreferredWidth(50);
      
        
        
        
          setCellRender(tb_consulta);
          conexion.desconectar();

        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e, "Error", JOptionPane.ERROR_MESSAGE);
        }
}

   public void LimpiarTabla()
    {
  
        for(int i=modelo.getRowCount()-1;i>=0;i--)
        {
            modelo.removeRow(i);
            
        }
    }
   // color a la tabla
   public void setCellRender(JTable table) {
        Enumeration<TableColumn> en = table.getColumnModel().getColumns();
        while (en.hasMoreElements()) {
            TableColumn tc = en.nextElement();
            tc.setCellRenderer(new CellRenderer());
        }
    }
   
   
    public void filtro() {
        int columnaABuscar = 0;
        
        
           if (cb_filtro.getSelectedItem() == "ID") {
            columnaABuscar = 0;
        }
       
        if (cb_filtro.getSelectedItem() == "Mes") {
            columnaABuscar = 2;
        }
        
        if (cb_filtro.getSelectedItem() == "Año") {
            columnaABuscar = 3;
        }
      
        
        trsFiltro.setRowFilter(RowFilter.regexFilter(txt_filtro.getText(), columnaABuscar));

    }

