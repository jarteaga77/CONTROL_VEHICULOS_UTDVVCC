    public void buscarConductor(int id_conductor)
    {
        
        
       String nombre="",apellido="", tel="",cel="", jefe="", direccion="", eps="", arl="";
       long cedula = 0;
       
        
       ResultSet obj=conexion.executeQuery("SELECT id_conductor,cedula, nombre, apellido, celular, tel, jefe_inmediato, direccion, eps,arl FROM utilidades.conductor WHERE id_conductor="+id_conductor+"");
        
        
        
        try {
            if(obj.next()==true)
            {
                cedula=obj.getLong("cedula");
                nombre=obj.getString("nombre");
                apellido=obj.getString("apellido");
                cel=obj.getString("celular");
                tel=obj.getString("tel");
                jefe=obj.getString("jefe_inmediato");
                direccion=obj.getString("direccion");
                eps=obj.getString("eps");
                arl=obj.getString("arl");
        
                        
            }
            
           
         txt_nombre.setText(nombre);
         txt_apellido.setText(apellido);
         txt_cedula.setText(String.valueOf(cedula));
         txt_cel.setText(cel);
         txt_tel.setText(tel);
         txt_dir.setText(direccion);
         txt_jefe.setText(jefe);
         txt_eps.setText(eps);
         txt_arl.setText(arl);
     
     
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(JIFConductor.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    
    }
         
