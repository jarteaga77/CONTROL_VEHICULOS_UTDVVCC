    private void btnadjuntarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnadjuntarActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileFilter( new FileNameExtensionFilter("Archivo de Imagen","jpg") );
        fileChooser.setCurrentDirectory(new java.io.File("e:/imagenes_tmp/"));
        int result = fileChooser.showOpenDialog(null);
        if ( result == JFileChooser.APPROVE_OPTION ){
            //obtiene ruta y nombre del archivo
            String ruta = fileChooser.getSelectedFile().getAbsolutePath();
            String name = fileChooser.getSelectedFile().getName();
            Conductor c=(Conductor) cb_registros.getSelectedItem();
            id_conductor_img=c.getId_conductor();
            conexion=new ConexionBD();
            try {
                
                ResultSet obj =conexion.executeQuery("SELECT foto FROM utilidades.conductor WHERE foto=true AND id_conductor="+id_conductor_img+"");
                
                        
                        if (obj.next()==true)
                        {
                            JOptionPane.showMessageDialog(rootPane, "No se puede cargar la foto. El vehiculo/Equpo ya tiene foto asignada.");
                        }
                  else
                        {
                if( mi.guardarfotoCon(GenerarCodigo() , name, ruta, id_conductor_img) )
                {
                    JOptionPane.showMessageDialog(this,"Documento ["+name+"] almacenado");
                    Actualizar_lista();
                   
                    
                    conexion.executeUpdateQuery("UPDATE utilidades.conductor SET foto=true WHERE id_conductor="+id_conductor_img+"");
                    conexion.desconectar();
                 
                }
              } 
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex, "Error",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnadjuntarActionPerformed

