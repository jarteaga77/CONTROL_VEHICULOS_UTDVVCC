public void consultar() throws SQLException
{
  try
        {
        
        conexion=new ConexionBD();
        
        
        ResultSet obj=conexion.executeQuery("SELECT mto.id_mto_interno,mto.consecutivo_vehiculo, mto.fecha_entrada,(v.placa_identi ||'-'|| m.descripcion ||'  '|| l.descripcion)AS vehiculo,r.descripcion, tm.descripcion, mec.nombre_apellido,salida_almacen, CASE WHEN mto.mto_externo=true THEN 'SI' ELSE 'NO' END AS mto_externo FROM mantenimientos_mecanica.mto_internos mto JOIN utilidades.vehiculos_equipos v ON v.id_vehiculo=mto.id_vehiculo \n" +
"JOIN utilidades.linea_vehiculo l ON l.id_linea_vehi=v.id_linea\n" +
"JOIN utilidades.marca_vehiculo m ON m.id_marca=l.id_marca\n" +
"JOIN utilidades.rutina r ON mto.id_rutina=r.id_rutina \n" +
"JOIN utilidades.tipo_mto tm ON tm.id_tipo_mto=mto.id_tipo_mto\n" +
"JOIN utilidades.mecanico_utd mec ON mec.id_meca=mto.id_mecanico\n" +
"\n" +
"ORDER BY mto.id_mto_interno DESC");
    
        
         while (obj.next()) 
        {
        
        Object [] datos = new Object[9];
        
     
        for (int i=0;i<9;i++)
        {
            datos[i] =obj.getObject(i+1);
        }

         modelo.addRow(datos);
       
        }
           tb_consulta.setModel(modelo);
           
        TableColumnModel columnModel =tb_consulta.getColumnModel();
        columnModel.getColumn(0).setPreferredWidth(5);
        columnModel.getColumn(1).setPreferredWidth(5);
        columnModel.getColumn(2).setPreferredWidth(15);
        columnModel.getColumn(3).setPreferredWidth(100);
        columnModel.getColumn(4).setPreferredWidth(30);
        columnModel.getColumn(5).setPreferredWidth(30);
        columnModel.getColumn(6).setPreferredWidth(50);
        columnModel.getColumn(7).setPreferredWidth(10);
        
        
        
          setCellRender(tb_consulta);
          conexion.desconectar();

        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e, "Error", JOptionPane.ERROR_MESSAGE);
        }
}

   public void LimpiarTabla()
    {
  
        for(int i=modelo.getRowCount()-1;i>=0;i--)
        {
            modelo.removeRow(i);
            
        }
    }
   // color a la tabla
   public void setCellRender(JTable table) {
        Enumeration<TableColumn> en = table.getColumnModel().getColumns();
        while (en.hasMoreElements()) {
            TableColumn tc = en.nextElement();
            tc.setCellRenderer(new CellRenderer());
        }
    }
   
   
    public void filtro() {
        int columnaABuscar = 0;
        if (cb_filtro.getSelectedItem().toString() == "ID") {
            columnaABuscar = 0;
        }
        if (cb_filtro.getSelectedItem() == "Consecutivo") {
            columnaABuscar = 1;
        }
        
        if (cb_filtro.getSelectedItem() == "Fecha") {
            columnaABuscar = 2;
        }
        if (cb_filtro.getSelectedItem() == "Vehiculo/Equipo") {
            columnaABuscar = 3;
        }
        
         if (cb_filtro.getSelectedItem() == "Rutina") {
            columnaABuscar = 4;
        }
        if (cb_filtro.getSelectedItem() == "Tipo Mto") {
            columnaABuscar = 5;
        }
        if (cb_filtro.getSelectedItem() == "Mecánico") {
            columnaABuscar = 6;
        }
        
         if (cb_filtro.getSelectedItem() == "Salida Almacén") {
            columnaABuscar = 7;
        }
        
        trsFiltro.setRowFilter(RowFilter.regexFilter(txt_filtro.getText(), columnaABuscar));

    }

