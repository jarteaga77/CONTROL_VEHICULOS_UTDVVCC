    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        
        try {
            int id_sistema=0;
            
            item=new ItemSistema();
            daoItemSistema=new DaoItemSistema();
            
            
            item.setItem(txt_descripcion.getText());
            item.setId_sistema(id_sistema);
            
            
            Sistemas s=(Sistemas)cb_sistema.getSelectedItem();
            id_sistema=s.getId();
            
            item.setId_sistema(id_sistema);
            
            daoItemSistema.guardar(item);
            
            limpiar();
            
            JOptionPane.showMessageDialog(rootPane, "Se registro con exito el nuevo sistema Automotor!");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane,ex);
        
        }
    
    }//GEN-LAST:event_btn_guardarActionPerformed

