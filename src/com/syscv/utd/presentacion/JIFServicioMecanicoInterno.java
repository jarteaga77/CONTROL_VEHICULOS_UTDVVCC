            public void cargarItemRutina()
       {
           ControllerItemList control=new ControllerItemList();
           ArrayList lista=control.listarItemRutina();
           
           if(lista !=null)
           {
               int size=lista.size();
               for(int i=0; i<size; i++)
            {
                cb_rutina.addItem(lista.get(i));
            }
           }
           
           
       }
            
        public void limpiar()
        {
            txt_descripcion.setText("");
            txt_costo.setText("");
            txt_duracion.setText("");
        }


