    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        
        try {
            int id_rutina=0;
            
            servicio=new Servicio_mec_interno();
            daoServicio=new DaoServicio_Mecanico();
            
            
            servicio.setDescripcion(txt_descripcion.getText());
            servicio.setTiempo_servicio(Float.parseFloat(txt_duracion.getText()));
            servicio.setCosto(Integer.parseInt(txt_costo.getText()));
            
            Rutina_mto r=(Rutina_mto) cb_rutina.getSelectedItem();
            id_rutina=r.getRutina();
            
            servicio.setId_rutina(id_rutina);
            
            daoServicio.guardar(servicio);
            
            limpiar();
            
            JOptionPane.showMessageDialog(rootPane, "Se registro con exito el nuevo servicio del Mecánico Interno !");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane,ex);
        
        }
    
    }//GEN-LAST:event_btn_guardarActionPerformed

