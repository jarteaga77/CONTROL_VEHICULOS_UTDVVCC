    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
         
        
        int costo;
        String servicio;
        double dur_servicio;
       
       
        
        if(tb_mecanico.isRowSelected(tb_mecanico.getSelectedRow()))
            {
                id = (Integer) tb_mecanico.getValueAt(tb_mecanico.getSelectedRow(), 0);    
                servicio=(String) tb_mecanico.getValueAt(tb_mecanico.getSelectedRow(), 1);
                dur_servicio= (double) tb_mecanico.getValueAt(tb_mecanico.getSelectedRow(), 2);
                costo=(int) tb_mecanico.getValueAt(tb_mecanico.getSelectedRow(), 3);
                
                
                txt_descripcion.setText(servicio);
                txt_duracion.setText(String.valueOf(dur_servicio));
                txt_costo.setText(String.valueOf(costo));
          
                 JDMod_Mecanico.pack();
                 JDMod_Mecanico.setLocationRelativeTo(null);
                 JDMod_Mecanico.setVisible(true);
                 JDMod_Mecanico.setResizable(false);
     
            }else
            {
                JOptionPane.showMessageDialog(null, "Debe seleccionar una fila de la tabla!", "Alerta!", JOptionPane.WARNING_MESSAGE);
            }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed

        try {
            int id_rutina=0;

            servicio=new Servicio_mec_interno();
            daoServicio=new DaoServicio_Mecanico();

            servicio.setDescripcion(txt_descripcion.getText());
            servicio.setTiempo_servicio(Float.parseFloat(txt_duracion.getText()));
            servicio.setCosto(Integer.parseInt(txt_costo.getText()));

            Rutina_mto r=(Rutina_mto) cb_rutina.getSelectedItem();
            id_rutina=r.getRutina();

            servicio.setId_rutina(id_rutina);
            servicio.setId_servicio(id);

            daoServicio.actualizar(servicio);

            limpiar();
            LimpiarTabla();
            consultarTodos();
            

            JOptionPane.showMessageDialog(rootPane, "Se registro con exito el nuevo servicio del Mecánico Interno !");
            JDMod_Mecanico.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane,ex);

        }

    }//GEN-LAST:event_btn_guardarActionPerformed

