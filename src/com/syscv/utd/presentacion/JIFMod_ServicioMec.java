       public void consultarTodos() throws SQLException
{
    
        ConexionBD conexion = new ConexionBD();
    
        ResultSet obj=conexion.executeQuery("SELECT serv.id_labor, serv.labor,serv.dur_labor,costo, rut.descripcion FROM utilidades.labor_mecanico serv JOIN utilidades.rutina rut ON rut.id_rutina=serv.id_rutina order by labor ASC");
    
    
        while (obj.next()) 
          {
          
          Object [] datos = new Object[5];
       
          for (int i=0;i<5;i++)
          {
              datos[i] =obj.getObject(i+1);
          }

           modelo.addRow(datos);
         
          }
        tb_mecanico.setModel(modelo);
        setCellRender(tb_mecanico);
        conexion.desconectar();
    
}
    
    public void LimpiarTabla()
    {
  
        for(int i=modelo.getRowCount()-1;i>=0;i--)
        {
            modelo.removeRow(i);
            
        }
    }
   
        public void setCellRender(JTable table) {
        Enumeration<TableColumn> en = table.getColumnModel().getColumns();
        while (en.hasMoreElements()) {
            TableColumn tc = en.nextElement();
            tc.setCellRenderer(new CellRenderer());
        }
    }


     public void limpiar()
    {
        txt_descripcion.setText("");
        txt_costo.setText("");
        txt_duracion.setText("");
       
   }


            public void cargarItemRutina()
       {
           ControllerItemList control=new ControllerItemList();
           ArrayList lista=control.listarItemRutina();
           
           if(lista !=null)
           {
               int size=lista.size();
               for(int i=0; i<size; i++)
            {
                cb_rutina.addItem(lista.get(i));
            }
           }
           
           
       } 
            
            
        public void filtro() {
        int columnaABuscar = 0;
        if (cb_filtro.getSelectedItem().toString() == "ID") {
            columnaABuscar = 0;
        }
        if (cb_filtro.getSelectedItem() == "Servicio") {
            columnaABuscar = 1;
        }
        
        if (cb_filtro.getSelectedItem() == "Rutina") {
            columnaABuscar = 4;
        }
       
        
        trsFiltro.setRowFilter(RowFilter.regexFilter(txt_filtro.getText(), columnaABuscar));

    }
  
