    private void btn_guardar_enActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardar_enActionPerformed
       
         try
         {
            int id_rutina=0;
            int id_tipo_mto=0;
            int id_mecanico=0;
            int id_usuario=0;
         
            Date fecha_llegada, fecha_salida;
            
            
            mto=new Mantenimiento_Interno();
            mtoInterno=new Dao_Mto_Interno();
            
            fecha_llegada=dc_fecha_llegada.getDate();
            fecha_salida=dc_salida.getDate();
            
            
           if(Integer.parseInt(txt_km_llegada.getText()) >= Integer.parseInt(txt_prox_mto.getText()))
           {
               throw new Exception("El Km de llegada no debe ser igual o mayor al km del proximo Mantenimiento.");
           }
           
           if(fecha_salida.before(fecha_llegada))
           {
             throw new Exception("La Fecha de Salida no puede ser inferior a la fecha de Entrada ");

           }
           
           if(horasSalida().before(horasllegada()))
           {
              throw new Exception("La Hora de salida no puede ser menor que la Hora de entrada");

           }
            
            Rutina_mto r=(Rutina_mto) cb_rutina.getSelectedItem();
            id_rutina=r.getRutina();
            
            Tipo_Mto t=(Tipo_Mto) cb_tipo.getSelectedItem();
            id_tipo_mto=t.getId();
            
            Mecanico_UTD m=(Mecanico_UTD) cb_mecanico.getSelectedItem();
            id_mecanico=m.getId_meca();
            
            id_usuario=MDIPrincipal.getvSeccion();
            
            mto.setKm_llegada(Integer.parseInt(txt_km_llegada.getText()));
            mto.setHoras_uso(Integer.parseInt(txt_horas.getText()));
            mto.setKm_prox_mto(Integer.parseInt(txt_prox_mto.getText()));
            mto.setId_vehiculo(id_vehiculo);
            mto.setId_rutina(id_rutina);
            mto.setId_t_mto(id_tipo_mto);
            mto.setId_mecanico(id_mecanico);
            
            mto.setHora_entrada(horasllegada());
            mto.setHora_salida(horasSalida());
            mto.setConsecutivo_vehiculo(consecutivo);
            
            mto.setFecha_entrada(fecha_llegada);
            mto.setFecha_salida(fecha_salida);
            mto.setId_usuario(id_usuario);
            mto.setFecha_reg(new Date());
            
            
            
            if(verificar==true)
            {
               mto.setSalida_almacen(Integer.parseInt(txt_rem_almacen.getText()));

            }
            else
            {
                throw new Exception("Verifique la Salida de Almacen correspondiente a este Mto.");
            }
            
            if(cbx_requiere.isSelected())
            {
                mto.setMto_externo(true);
            }
            else
                {
                    mto.setMto_externo(false);
                }
          
            
            
            
            mtoInterno.guardar(mto);
            idmtointerno= Dao_Mto_Interno.getIdMtoInterno();
            txt_id_mto.setText(String.valueOf(idmtointerno));
            conexion.executeUpdateQuery("UPDATE utilidades.vehiculos_equipos SET consecutivo_mto_interno="+txt_consecutivo.getText()+" WHERE id_vehiculo="+id_vehiculo+"");

            btn_guardar_en.setEnabled(false);
            cb_vehiculo.setEditable(false);
            
             cargarItemServicio(mto.getId_rutina());
             
             btn_agregar.setEnabled(true);
            
            
            
            
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Error " + ex);
        }
         
        
         
        
        
        
    }//GEN-LAST:event_btn_guardar_enActionPerformed

    private void cb_vehiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cb_vehiculoActionPerformed
        try {
            Vehiculo v=(Vehiculo) cb_vehiculo.getSelectedItem();
            id_vehiculo=v.getId_vehiculo();
            

            ResultSet obj=conexion.executeQuery("SELECT consecutivo_mto_interno+1 AS consecutivo FROM utilidades.vehiculos_equipos WHERE id_vehiculo="+id_vehiculo+" ");
            
            if(obj.next())
            {
                consecutivo=obj.getInt("consecutivo");
                
                txt_consecutivo.setText(String.valueOf(consecutivo));
            }
        } catch (SQLException ex) {
            Logger.getLogger(JIFTallerInterno.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cb_vehiculoActionPerformed

