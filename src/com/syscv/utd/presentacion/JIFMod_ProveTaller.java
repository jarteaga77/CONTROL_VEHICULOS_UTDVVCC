    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed

        try
        {

            taller=new TallerExterno();
            daoTaller=new DaoTallerExterno();

            if(txt_nit.getText().isEmpty() || txt_razon_social.getText().isEmpty())
            {
                throw new Exception("Algunos de los campos obligatorios estan vacios");
            }

            if(txt_cel.getText().length() > 10)
            {
                throw new Exception("El campo celular solo permite diez números");

            }
            if(txt_tel.getText().length() > 7)
            {
                throw new Exception("El campo telefono solo permite siete números");

            }
            
            
            taller.setDireccion(txt_dir.getText());
            taller.setCelular(Long.parseLong(txt_cel.getText()));
            taller.setTel(Integer.parseInt(txt_tel.getText()));
            taller.setCiudad(txt_ciudad.getText());
            taller.setAccesor(txt_asesor.getText());
            taller.setObservacion(txarea.getText());
            taller.setId(id);
            
            daoTaller.actualizar(taller);
            
            LimpiarTabla();
            consultarTodos();
            
            JOptionPane.showMessageDialog(rootPane, "Se Actualizó el Proveedor / Taller con exito!");
            limpiar();
            JDMod_Mecanico.dispose();

        }catch(Exception ex )
        {
            JOptionPane.showMessageDialog(rootPane, ex);
        }
    }//GEN-LAST:event_btn_guardarActionPerformed

