     public void filtro() {
        int columnaABuscar = 0;
        if (cb_filtro.getSelectedItem().toString() == "NIT") {
            columnaABuscar = 1;
        }
        if (cb_filtro.getSelectedItem() == "Razón Social") {
            columnaABuscar = 2;
        }
        
      
        
        trsFiltro.setRowFilter(RowFilter.regexFilter(txt_filtro.getText(), columnaABuscar));

    }








