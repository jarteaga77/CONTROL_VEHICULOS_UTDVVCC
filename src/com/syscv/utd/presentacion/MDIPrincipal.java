 public void logueo()
 {
     try
     {
         int level=0;
         String usuario=txt_usuario.getText();
         String contra=String.valueOf(pf_contra.getPassword());
         
         ResultSet obj=conexion.executeQuery("SELECT id_usuario, usuario, id_level FROM utilidades.usuarios WHERE usuario='"+usuario+"' AND passw='"+contra+"'");
         
         if(obj.next()==true)
         {
             JDInicio.dispose();
             txt_usuario.setText("");
             pf_contra.setText("");
             
             level=obj.getInt("id_level");
             
             if(level==2)
             {
                 mi_aproce.setEnabled(false);
                 mi_aproRC.setEnabled(false);
                 m_utilidades.setEnabled(false);
                 m_reporte_mensual.setEnabled(false);
                 m_report_eds.setEnabled(false);
                 
                  this.setVisible(true);
             
             int id=obj.getInt("id_usuario");
             
             System.out.println(id);
             usuario=obj.getString("usuario");
             
             lb_usuario.setText(usuario);
             
             setvSeccion(id);
             setvSeccionNom(usuario);
                 
             }
             else
             {
             
             this.setVisible(true);
             
             int id=obj.getInt("id_usuario");
             
             System.out.println(id);
             usuario=obj.getString("usuario");
             
             lb_usuario.setText(usuario);
             
             setvSeccion(id);
             setvSeccionNom(usuario);

          }
             
         }
         else
         {
             JOptionPane.showMessageDialog(this, "Usuario o contraseña incorrectos. \n Intentelo de nuevo", "Mensaje Error", JOptionPane.ERROR_MESSAGE);
         }
         
     }catch(SQLException | HeadlessException e)
     {
        JOptionPane.showMessageDialog(this, "Error de conexión" + e, "Mensaje Error", JOptionPane.ERROR_MESSAGE);

     }
 }
    
    
    
private boolean estacerrado(Object obj)
{
    JInternalFrame[] activos=escritorio.getAllFrames();
    boolean cerrado=true;
    int i=0;
    while (i<activos.length && cerrado)
    {
        if(activos[i]==obj)
        {
            cerrado=false;
        }
        i++;
    }
    return cerrado;
    
}

