    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        if(estacerrado(ventanaControl))
        {
            ventanaControl=new JIFControlCombustible();
            escritorio.add(ventanaControl);
            Dimension tamanoEscritorio=escritorio.getSize();
            Dimension framesize=ventanaControl.getSize();
            ventanaControl.setLocation((tamanoEscritorio.width - framesize.width)/2, (tamanoEscritorio.height - framesize.height)/2) ;
            ventanaControl.show();
       
        }else
        {
            JOptionPane.showMessageDialog(this, "La ventana ya esta abierta");
        }
    }//GEN-LAST:event_jMenuItem2ActionPerformed

